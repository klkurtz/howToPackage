#' firstFunction
#'
#'firstFunction sums two inputs
#'
#' @param x a scalar or vector
#' @param y a scalar or vector
#'
#' @return returns the sum of the two parameters
#' @export
firstFunction <- function(x, y){
  sum <- x+y
  return(sum)
}



